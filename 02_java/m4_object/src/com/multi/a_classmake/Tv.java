package com.multi.a_classmake;

public class Tv {
    private int ch;
    private int vol;
    private boolean onOff;

    public int getCh() {
        return ch;
    }
    public void setCh(int ch) {
        this.ch = ch;
    }

    public int getVol() {
        return vol;
    }
    public void setVol(int vol) {
        this.vol = vol;
    }

    public boolean getOnOff() {
        return onOff;
    }
    public void setOnOff(boolean onOff) {
        this.onOff = onOff;
    }
}
